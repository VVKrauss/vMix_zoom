В личных чатах, группах и каналах можно отправлять голосовые сообщения: удерживаете кнопку микрофона, говорите и отпускаете, чтобы отправить (сдвиг влево — отмена). Удобно, когда быстрее сказать, чем печатать.

Работа с каналами и группами стала спокойнее: меньше лишних перерисовок, лента канала и комментарии ведут себя предсказуемее при прокрутке и приходе новых сообщений.

Картинки в ленте и в переписке кэшируются на устройстве — повторный заход открывает уже просмотренное быстрее, с меньшей нагрузкой на сеть. Для вложений подняли допустимый размер файла, чтобы реже упираться в лимит при качественных фото.

Push-уведомления подправили: отдельная иконка для строки состояния и баннера, чтобы бренд не «терялся» на светлой теме телефона.

Для личных диалогов добавлена наглядная индикация «прочитано»: сообщение помечается прочитанным, когда вы действительно видите конец переписки в открытом чате, а не только открыли экран.
