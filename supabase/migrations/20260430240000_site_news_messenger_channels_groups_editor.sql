В мессенджере можно создавать групповые чаты и каналы: обсуждения в группе или лента публикаций в канале — в одном интерфейсе с личными сообщениями.

Для постов в каналах появился удобный редактор: форматирование текста, вставка изображений и предпросмотр перед публикацией, чтобы собирать посты быстрее и нагляднее.
$news$
where not exists (
  select 1
  from public.site_news sn
  where sn.title = 'Мессенджер: каналы, группы и редактор постов'
);
