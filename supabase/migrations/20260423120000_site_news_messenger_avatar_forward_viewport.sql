Локальные аватары контактов: так же, как отображаемое имя, можно задать свой URL картинки для человека из контактов — в карточке профиля и в списке личных чатов будет показываться ваш вариант.

Пересланные сообщения: строка «Переслано из …» стала интерактивной, если мы знаем источник — переход к посту или комментарию в канале, к сообщению в группе или к профилю автора в личке.

Мобильные устройства и iPad: после возврата в приложение пересчитывается видимая область экрана, а меню сообщения позиционируется с учётом visual viewport — меньше пустого места под «клавиатуру» и меньше съезда окон в угол.
$news$
