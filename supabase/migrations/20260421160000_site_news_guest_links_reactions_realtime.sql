Сделали «лёгкий вход» и публичные ссылки.

Теперь можно открыть профиль пользователя по ссылке без регистрации: посмотреть карточку и сразу на этой же странице быстро зарегистрироваться/войти через плавающую панель. После входа вы остаётесь на текущей странице и можете продолжать работу в мессенджере.

Добавили публичное превью открытых групп/каналов по ссылке: видно информацию о чате и последние сообщения. Чтобы вступить и писать — нужно войти.

Реакции стали аккуратнее: убрали фон/контур у чипов и добавили новые эмодзи (обнять, поцеловать, язык, смущение, подмигнуть).

Исправили несколько неприятных моментов в реальном времени:
- при прочтении на другом устройстве бейджи непрочитанных в дереве чатов теперь синхронно сбрасываются;
- индикатор «онлайн» стал стабильнее и одинаково работает в дереве и в шапке диалога.

И ещё: подписи к фото теперь корректно переносятся на новую строку.
$news$
where not exists (
  select 1
  from public.site_news sn
  where sn.title = 'Мессенджер: гостевые ссылки, реакции и синхронизация'
